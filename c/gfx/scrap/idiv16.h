//this one approximates with [-1;0] err range in quotient
static uint32_t idiv_approx(uint32_t n, int d) {
  return (n*idiv_lut[d])>>16;
}

static uint32_t idiv(uint32_t n, int d) {
  int r = (n*idiv_lut[d])>>16; //approximate down [-1;0]
  int k = r*d;
  if (k == n) return r; //78% of cases
  k += d;
  r += 1;
  if (k < n) {
    k += d;
    r += 1;
  }
  if (k != n) r -= 1; //round towards zero
  return r;
}

